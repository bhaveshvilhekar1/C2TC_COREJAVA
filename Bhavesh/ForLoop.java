/*
    This program show the working of FOR LOOP
    Syntax: for(starting point; condition; increment/decrement){
                //code
            }
*/
public class ForLoop {
    public static void main(String[] args) {	
        for(int i = 1; i <= 1000 ; i++) { // (int i = 1000; i >= 1; i--) 

			System.out.print("Value of i: "); // System.out.println("Value of i:" + i);
			System.out.println(i);
		}
	}  
}

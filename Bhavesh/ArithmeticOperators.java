// This Program shows increment and decrement
public class ArithmeticOperators {
    public static void main(String[] args) {
		
		int firstNum = 10;// First Number
        int secondNum = 20;// Second Number

		System.out.println(firstNum + secondNum);//Adding two numbers

		System.out.println(--firstNum);// Pre-Decrementing

		System.out.println(++secondNum);// Pre-Incrementing

	}    
}

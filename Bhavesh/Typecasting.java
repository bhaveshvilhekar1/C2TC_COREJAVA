
/*
This shows how we can typecast one datatype to another. Basically typcasting means
coverting one datatype to another
*/
public class Typecasting {
    public static void main(String[] args) {
		
        float floatingPointNumber = 10.532f;// Initializing float value
        long longDatatype = (long) floatingPointNumber;// Typecasting it to 
        System.out.println(longDatatype);
    }
}

// This program shows the use of boolean and unicode characters
public class BooleanAndUnicode {
    public static void main(String[] args) {
		
        boolean bool = false; // Boolean stores true or false value
		System.out.println(bool); 

		char character = '\u00A7'; //char can store only 1 byte i.e, only one character
		System.out.println(character);	
	}
}

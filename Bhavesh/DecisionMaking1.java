//This program shows the working of "!=" using if-else statements
public class DecisionMaking1 {
	public static void main(String[] args) {
        
        int number = 5;//Initializing the number

        if(number != 5) {// This is a false condition so it will not execute
            System.out.println("Value is not 5");    
        }
        else {// This will execute
            System.out.println("Value is 5");
        }
	}
}
